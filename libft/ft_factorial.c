int	ft_factorial(int nb)
{
	int result;

	result = nb;
	if (nb < 0 || nb >= 13)
		return (0);
	if (nb == 0)
		return (1);
	while (nb > 1)
	{
		result *= nb - 1;
		nb--;
	}
	return (result);
}
