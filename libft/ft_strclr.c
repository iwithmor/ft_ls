#include "libft.h"

void	ft_strclr(char *s)
{
	size_t i;
	size_t n;

	if (!s)
		return ;
	i = 0;
	n = ft_strlen((const char *)s);
	if (n == 0)
		return ;
	while (n > 0)
	{
		s[n] = '\0';
		n--;
	}
	s[n] = '\0';
}
